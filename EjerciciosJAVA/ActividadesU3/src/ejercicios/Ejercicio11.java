package ejercicios;

public class Ejercicio11 {

	public static void main(String[] args) {
		
	        // Mostrar las tablas del 1 al 10
	        for (int numero = 1; numero <= 10; numero++) {
	            System.out.println("Tabla del " + numero + ":");
	            for (int i = 1; i <= 10; i++) {
	                System.out.println(numero + " x " + i + " = " + (numero * i));
	            }
	            
	        }
	    }}
	